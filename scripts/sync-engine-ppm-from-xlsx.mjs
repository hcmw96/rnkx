#!/usr/bin/env node
/**
 * Sync engine PPM lookup from RNKX_Engine_League_Lookup_UPDATED_60_to_72.xlsx
 * → scripts/engine-ppm-lookup.tsv
 * → src/lib/engineLeaguePpm.generated.ts
 * → supabase/migrations/20260626120000_engine_ppm_spreadsheet_sync.sql
 */
import fs from 'node:fs';
import path from 'node:path';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';

const root = path.join(path.dirname(fileURLToPath(import.meta.url)), '..');
const xlsxCandidates = [
  path.join(root, 'scripts/RNKX_Engine_League_Lookup_UPDATED_60_to_72.xlsx'),
  '/Users/henry/Downloads/RNKX_Engine_League_Lookup_UPDATED_60_to_72.xlsx',
];

const xlsxPath = xlsxCandidates.find((p) => fs.existsSync(p));
if (!xlsxPath) {
  throw new Error(`Spreadsheet not found. Tried: ${xlsxCandidates.join(', ')}`);
}

const py = spawnSync(
  'python3',
  [
    '-c',
    `
import openpyxl, json, sys
wb = openpyxl.load_workbook(sys.argv[1], data_only=True)
ws = wb['Engine Lookup Updated']
sheet = {}
for r in range(2, ws.max_row + 1):
    hr, ppm = ws.cell(r, 1).value, ws.cell(r, 2).value
    if hr is None or ppm is None or isinstance(hr, str):
        continue
    sheet[round(float(hr) * 10)] = float(ppm)
rows = []
for tenths in range(500, 1001):
    hr = tenths / 10
    ppm = sheet.get(tenths, 0.0)
    rows.append({'hr': hr, 'tenths': tenths, 'ppm': ppm})
print(json.dumps(rows))
`,
    xlsxPath,
  ],
  { encoding: 'utf8' },
);

if (py.status !== 0) {
  console.error(py.stderr);
  throw new Error('Failed to read xlsx via python');
}

const rows = JSON.parse(py.stdout.trim());
if (rows.length !== 501) {
  throw new Error(`Expected 501 rows (50.0–100.0), got ${rows.length}`);
}

const tsv = ['avg_hr_percent_max\tpoints_per_minute', ...rows.map((r) => `${r.hr}\t${r.ppm.toFixed(2)}`)].join('\n') + '\n';
fs.writeFileSync(path.join(root, 'scripts/engine-ppm-lookup.tsv'), tsv);

const ppmValues = rows.map((r) => r.ppm.toFixed(2));
fs.writeFileSync(
  path.join(root, 'src/lib/engineLeaguePpm.generated.ts'),
  `/** Auto-generated by scripts/sync-engine-ppm-from-xlsx.mjs — do not edit manually. */\nexport const ENGINE_PPM_LOOKUP_BASE_TENTHS = 500;\nexport const ENGINE_PPM_BY_HR_TENTHS: readonly number[] = [\n  ${ppmValues.join(', ')},\n] as const;\n`,
);

const inserts = rows.map((r) => `  (${r.tenths}, ${r.ppm.toFixed(2)})`).join(',\n');
const migration = `-- Sync engine_ppm_lookup to RNKX_Engine_League_Lookup_UPDATED spreadsheet (2026-06-10).
-- Also backfills Apple workout engine_score values and rebuilds engine totals.

insert into public.engine_ppm_lookup (hr_percent_tenths, ppm) values
${inserts}
on conflict (hr_percent_tenths) do update set ppm = excluded.ppm;

create or replace function public.engine_ppm_from_hr_percent(p_hr_percent numeric)
returns numeric
language plpgsql
stable
as $$
declare
  v_tenths integer;
  v_ppm numeric;
begin
  if p_hr_percent is null or p_hr_percent < 58 then
    return 0;
  end if;

  if p_hr_percent > 100 then
    return 5.20;
  end if;

  v_tenths := round(p_hr_percent * 10)::integer;
  select ppm into v_ppm from public.engine_ppm_lookup where hr_percent_tenths = v_tenths;
  return coalesce(v_ppm, 0);
end;
$$;

create or replace function public.backfill_engine_workout_scores()
returns jsonb
language plpgsql
security definer
set search_path = public
as $$
declare
  v_season_id uuid;
  v_workouts_updated integer := 0;
  v_capped_zeroed integer := 0;
begin
  select id into v_season_id from public.seasons where is_active = true limit 1;

  with updated as (
    update public.workouts w
    set engine_score = public.engine_league_session_score(
      (w.avg_hr / coalesce(nullif(a.max_hr, 0), 220 - a.age)) * 100,
      least(w.duration_min, 120)
    )
    from public.athletes a
    where a.id = w.athlete_id
      and w.status = 'scored'
      and coalesce(w.run_score, 0) = 0
      and w.avg_hr is not null
    returning w.id
  )
  select count(*) into v_workouts_updated from updated;

  with ranked as (
    select
      w.id,
      row_number() over (
        partition by w.athlete_id, date_trunc('day', w.started_at)::date
        order by w.engine_score desc nulls last, w.started_at desc
      ) as rn
    from public.workouts w
    where w.status = 'scored'
      and coalesce(w.engine_score, 0) > 0
  ),
  capped as (
    update public.workouts w
    set engine_score = 0
    from ranked r
    where w.id = r.id
      and r.rn > 2
    returning w.id
  )
  select count(*) into v_capped_zeroed from capped;

  update public.athletes a
  set total_score = coalesce(wsum.workout_pts, 0) + coalesce(cbonus.bonus, 0)
  from public.athletes a2
  left join (
    select athlete_id, coalesce(sum(engine_score + run_score), 0) as workout_pts
    from public.workouts
    where status = 'scored'
    group by athlete_id
  ) wsum on wsum.athlete_id = a2.id
  left join (
    select ast.athlete_id, ast.score as bonus
    from public.athlete_stats ast
    where ast.category = 'consistency'
      and ast.season_id = v_season_id
  ) cbonus on cbonus.athlete_id = a2.id
  where a.id = a2.id;

  if v_season_id is not null then
    update public.athlete_stats ast
    set score = coalesce(wsum.engine_pts, 0)
    from public.athlete_stats ast2
    left join (
      select athlete_id, coalesce(sum(engine_score), 0) as engine_pts
      from public.workouts
      where status = 'scored'
      group by athlete_id
    ) wsum on wsum.athlete_id = ast2.athlete_id
    where ast.athlete_id = ast2.athlete_id
      and ast.season_id = v_season_id
      and ast.category = 'engine';
  end if;

  return jsonb_build_object(
    'workouts_updated', v_workouts_updated,
    'capped_zeroed', v_capped_zeroed,
    'season_id', v_season_id
  );
end;
$$;

revoke all on function public.backfill_engine_workout_scores() from public;
grant execute on function public.backfill_engine_workout_scores() to service_role;

select public.backfill_engine_workout_scores();
`;

fs.writeFileSync(path.join(root, 'supabase/migrations/20260626120000_engine_ppm_spreadsheet_sync.sql'), migration);

console.log('Source:', xlsxPath);
console.log('Wrote scripts/engine-ppm-lookup.tsv');
console.log('Wrote src/lib/engineLeaguePpm.generated.ts');
console.log('Wrote supabase/migrations/20260626120000_engine_ppm_spreadsheet_sync.sql');
console.log('58% ppm:', rows.find((r) => r.tenths === 580)?.ppm, '| 90% ppm:', rows.find((r) => r.tenths === 900)?.ppm);
